	local entryId = this.weaponPartsCombinationSettings.idTable[i]
	local entryPartsType = this.weaponPartsCombinationSettings.partsTypeTable[i]
	local entryPartsId = this.weaponPartsCombinationSettings.partsIdTable[i]
	local entry = { receiverID=entryId,partsType=entryPartsType,partsIds=entryPartsId}	
	
	if func == 1 then TppMotherBaseManagement.RegistPartsInclusionInfo_ReceiverBase(entry) 
	elseif func == 2 then TppMotherBaseManagement.RegistPartsInclusionInfo(entry) 
	elseif func == 3 then 
		entry = { barrelID=entryId,partsType=entryPartsType,partsIds=entryPartsId}	
		TppMotherBaseManagement.RegistPartsInclusionInfo_BarrelBase(entry) 
	elseif func == 4 then TppMotherBaseManagement.RegistPartsInclusionInfo_ReceiverWithUnderBarrellBase(entry) 
	end
end

